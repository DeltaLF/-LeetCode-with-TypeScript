import { makeArrayIncreasing } from "../1187_Make_Array_Strictly_Increasing";
it("test", () => {
  expect(
    makeArrayIncreasing(
      [31, 18, 1, 12, 23, 14, 25, 4, 17, 18, 29, 28, 35, 34, 19, 8, 25, 6, 35],
      [
        13, 10, 25, 18, 3, 8, 37, 20, 23, 12, 9, 36, 17, 22, 29, 6, 1, 12, 37,
        6, 3, 14, 37, 2,
      ]
    )
  ).toBe(19);
});
