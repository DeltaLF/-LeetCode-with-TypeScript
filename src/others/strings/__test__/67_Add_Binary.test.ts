import { addBinary } from "../67_Add_Binary";

it("adds binary", () => {
  expect(addBinary("1", "0")).toBe("1");
  expect(addBinary("0", "0")).toBe("0");
  expect(addBinary("1", "1")).toBe("10");
  // two
  expect(addBinary("10", "0")).toBe("10");
  expect(addBinary("11", "0")).toBe("11");
  expect(addBinary("11", "1")).toBe("100");
  expect(addBinary("11", "11")).toBe("110");

  expect(
    addBinary(
      "10100000100100110110010000010101111011011001101110111111111101000000101111001110001111100001101",
      "110101001011101110001111100110001010100001101011101010000011011011001011101111001100000011011110011"
    )
  ).toBe(
    "110111101100010011000101110110100000011101000101011001000011011000001100011110011010010011000000000"
  );
});
